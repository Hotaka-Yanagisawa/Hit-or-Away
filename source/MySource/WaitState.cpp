void WaitState::Init(CEnemy *enemy)
{
}

void WaitState::Update(CEnemy *enemy, HierarchyState *parent)
{
	int time = enemy->AddTime(1);
	
	if (time > 30)
	{
		parent->ChangeState(enemy, MoveState::GetInstance());
	}
}
